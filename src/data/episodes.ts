export const getFeaturedEpisode = (): Episode | undefined =>
  episodes.find((ep) => ep.featured);

export const getEpisodeBySlug = (slug: string): Episode | undefined =>
  episodes.find((ep) => ep.slug === slug);

export const getEpisodeById = (id: number): Episode | undefined =>
  episodes.find((ep) => ep.id === id);

export const getNextEpisode = (currentId: number): Episode | undefined =>
  episodes.find((ep) => ep.id === currentId + 1);

export const getPreviousEpisode = (currentId: number): Episode | undefined =>
  episodes.find((ep) => ep.id === currentId - 1);

export const getLatestEpisodes = (count: number): Episode[] =>
  [...episodes].sort((a, b) => new Date(b.releaseDate).getTime() - new Date(a.releaseDate).getTime()).slice(0, count);
